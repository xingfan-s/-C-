// 创建新行函数
hang* 创建新行(const char* 内容) {
    hang* 新行 = (hang*)malloc(sizeof(hang));
    if (!新行) {
        printf("内存分配失败！\n");
        return NULL;
    }

    if (内容) {
        my_strcpy(新行->内容, 内容);
    }
    else {
        新行->内容[0] = '\0';
    }

    新行->下一行 = NULL;  // 确保初始化为NULL
    return 新行;
}

// 获取指定行
hang* 获取行(const wen* 文本, int 行号) {
    if (行号 < 1 || 行号 > 文本->总行数) {
        return NULL;
    }

    hang* 当前行 = 文本->首行;
    for (int i = 1; i < 行号; i++) {
        当前行 = 当前行->下一行;
    }

    return 当前行;
}
// 插入行函数
int 插入行(wen* 文本, int 行号, const char* 内容) {
    if (行号 < 1 || 行号 > 文本->总行数 + 1) {
        printf("错误：行号无效！有效范围：1-%d\n", 文本->总行数 + 1);
        return 0;
    }

    hang* 新行 = 创建新行(内容);
    if (!新行) {
        printf("错误：创建新行失败！\n");
        return 0;
    }

    // 如果链表为空，直接作为首行
    if (文本->首行 == NULL) {
        文本->首行 = 新行;
        文本->尾行 = 新行;
        文本->总行数++;
        文本->已修改 = 1;
        printf("插入成功！在第 %d 行插入内容。\n", 行号);
        return 1;
    }

    // 插入到第一行
    if (行号 == 1) {
        新行->下一行 = 文本->首行;
        文本->首行 = 新行;
        // 如果只有一行，更新尾行
        if (文本->总行数 == 0) {
            文本->尾行 = 新行;
        }
    }
    // 插入到最后一行之后
    else if (行号 == 文本->总行数 + 1) {
        // 确保尾行不为NULL
        if (文本->尾行 != NULL) {
            文本->尾行->下一行 = 新行;
            文本->尾行 = 新行;
        }
        else {
            // 如果尾行为NULL，需要找到最后一个节点
            hang* 当前行 = 文本->首行;
            while (当前行->下一行 != NULL) {
                当前行 = 当前行->下一行;
            }
            当前行->下一行 = 新行;
            文本->尾行 = 新行;
        }
    }
    // 插入到中间
    else {
        hang* 前一行 = 获取行(文本, 行号 - 1);
        if (前一行 == NULL) {
            free(新行);
            printf("错误：找不到指定位置！\n");
            return 0;
        }
        新行->下一行 = 前一行->下一行;
        前一行->下一行 = 新行;
        // 如果插入到原最后一行之前，需要更新尾行
        if (新行->下一行 == NULL) {
            文本->尾行 = 新行;
        }
    }

    文本->总行数++;
    文本->已修改 = 1;
    printf("插入成功！在第 %d 行插入内容。\n", 行号);
    return 1;
}
// 修改行函数
int 修改行(wen* 文本, int 行号, const char* 新内容) {
    if (行号 < 1 || 行号 > 文本->总行数) {
        printf("错误：行号无效！有效范围：1-%d\n", 文本->总行数);
        return 0;
    }

    hang* 要修改的行 = 获取行(文本, 行号);
    if (!要修改的行) {
        printf("错误：找不到指定行！\n");
        return 0;
    }

    my_strcpy(要修改的行->内容, 新内容);
    文本->已修改 = 1;
    printf("修改成功！第 %d 行已更新。\n", 行号);
    return 1;
}
// 显示内容
void 显示内容(const wen* 文本) {
    if (文本->总行数 == 0) {
        printf("文件为空\n");
        return;
    }

    printf("\n=== 文件内容 ===\n");
    hang* 当前行 = 文本->首行;
    int 行号 = 1;

    while (当前行 != NULL) {
        printf("%3d: %s\n", 行号, 当前行->内容);
        当前行 = 当前行->下一行;
        行号++;
    }
    printf("================\n\n");
}

// 保存文件
int 保存文件(const wen* 文本) {
    if (my_strlen(文本->文件名) == 0) {
        printf("未指定文件名！\n");
        return 0;
    }

    FILE* 文件指针 = NULL;
    errno_t 错误 = fopen_s(&文件指针, 文本->文件名, "w");//返回0 表示成功，非 0 表示错误

    if (错误 != 0 || 文件指针 == NULL) {
        printf("无法保存文件: %s\n", 文本->文件名);
        return 0;
    }

    hang* 当前行 = 文本->首行;
    while (当前行 != NULL) {
        fprintf(文件指针, "%s\n", 当前行->内容);
        当前行 = 当前行->下一行;
    }

    fclose(文件指针);
    printf("文件已保存: %s\n", 文本->文件名);
    return 1;
}

// 释放内存
void 释放内存(wen* 文本) {
    hang* 当前行 = 文本->首行;
    while (当前行 != NULL) {
        hang* 下一行 = 当前行->下一行;
        free(当前行);
        当前行 = 下一行;
    }

    文本->首行 = NULL;
    文本->尾行 = NULL;
    文本->总行数 = 0;
    文本->已修改 = 0;
}

// 初始化编辑器
void 初始化编辑器(wen* 文本) {
    文本->首行 = NULL;
    文本->尾行 = NULL;
    文本->总行数 = 0;
    my_memset(文本->文件名, 0, MAX_文件名);
    文本->已修改 = 0;
}

// 打开文件函数
int 打开文件(wen* 文本, const char* 文件名) {
    // 检查文件名是否为空
    if (文件名 == NULL || my_strlen(文件名) == 0) {
        printf("错误：文件名不能为空！\n");
        return 0;
    }

    // 检查是否已经有未保存的修改
    if (文本->已修改 && my_strlen(文本->文件名) > 0) {
        printf("警告：当前文件有未保存的修改！\n");
        printf("是否保存当前文件？(y/n): ");
        char 选择;
		scanf_s(" %c", &选择, 1);  

        while (getchar() != '\n');  // 清空输入缓冲区

        if (选择 == 'y' || 选择 == 'Y') {
            if (!保存文件(文本)) {
                printf("保存失败，取消打开新文件\n");
                return 0;
            }
        }
    }

    // 打开文件
    FILE* 文件指针 = NULL;
    errno_t 错误 = fopen_s(&文件指针, 文件名, "r");

    if (错误 != 0 || 文件指针 == NULL) {
        // 文件不存在，询问是否创建新文件
        printf("文件不存在，是否创建新文件？(y/n): ");
        char 选择;
        scanf_s(" %c", &选择, 1);  

        while (getchar() != '\n');  // 清空输入缓冲区

        if (选择 == 'y' || 选择 == 'Y') {
            // 创建新文件
            错误 = fopen_s(&文件指针, 文件名, "w");
            if (错误 != 0 || 文件指针 == NULL) {
                printf("错误：无法创建文件！\n");
                return 0;
            }
            fclose(文件指针);
            printf("已创建新文件: %s\n", 文件名);

            // 清空当前编辑器内容
            释放内存(文本);
            初始化编辑器(文本);

            // 保存文件名
            my_strcpy(文本->文件名, 文件名);
            文本->已修改 = 0;

            return 1;
        }
        else {
            printf("取消打开文件\n");
            return 0;
        }
    }
    else {
        // 文件存在，读取内容
        printf("正在打开文件: %s\n", 文件名);

        // 清空当前编辑器内容
        释放内存(文本);
        初始化编辑器(文本);

        // 在打开文件函数中，修改以下部分：

        // 读取文件内容到链表
        char 缓冲区[MAX_行];
        hang* 最后一行 = NULL;

        while (fgets(缓冲区, MAX_行, 文件指针) != NULL) {
            // 移除行尾的换行符
            int 长度 = my_strlen(缓冲区);
            if (长度 > 0 && 缓冲区[长度 - 1] == '\n') {
                缓冲区[长度 - 1] = '\0';
            }

            // 创建新行
            hang* 新行 = 创建新行(缓冲区);
            if (!新行) {
                fclose(文件指针);
                printf("错误：内存不足！\n");
                return 0;
            }

            // 添加到链表
            if (文本->首行 == NULL) {
                文本->首行 = 新行;
                最后一行 = 新行;
            }
            else {
                if (最后一行 != NULL) {
                    最后一行->下一行 = 新行;
                    最后一行 = 新行;
                }
            }
            文本->总行数++;
        }

        fclose(文件指针);

        // 确保尾行指针正确设置
        if (文本->首行 != NULL && 最后一行 != NULL) {
            文本->尾行 = 最后一行;
        }
        else {
            文本->尾行 = 文本->首行; // 如果只有一行或没有行
        }

        // 保存文件名
        my_strcpy(文本->文件名, 文件名);
        文本->已修改 = 0;

        printf("成功打开文件！共 %d 行\n", 文本->总行数);
        return 1;
    }
}
int 删除行(wen* 文本, int 起始行, int 行数) {
    if (起始行 < 1 || 起始行 > 文本->总行数) {
        printf("错误：起始行号无效！有效范围：1-%d\n", 文本->总行数);
        return 0;
    }

    if (行数 <= 0) {
        printf("错误：要删除的行数必须大于0！\n");
        return 0;
    }

    // 计算实际可以删除的行数
    int 实际删除行数 = 行数;
    if (起始行 + 行数 - 1 > 文本->总行数) {
        实际删除行数 = 文本->总行数 - 起始行 + 1;
    }

    // 如果要删除第一行
    if (起始行 == 1) {
        for (int i = 0; i < 实际删除行数; i++) {
            hang* 要删除的行 = 文本->首行;
            if (要删除的行) {
                文本->首行 = 要删除的行->下一行;
                free(要删除的行);
                文本->总行数--;
            }
        }
    }
    else {
        // 找到起始行的前一行
        hang* 前一行 = 获取行(文本, 起始行 - 1);
        if (!前一行) {
            printf("错误：找不到要删除的起始行！\n");
            return 0;
        }

        // 删除指定行数的节点
        hang* 当前行 = 前一行->下一行;
        for (int i = 0; i < 实际删除行数 && 当前行 != NULL; i++) {
            hang* 下一行 = 当前行->下一行;
            free(当前行);
            当前行 = 下一行;
            文本->总行数--;
        }
        前一行->下一行 = 当前行;

        // 如果删除的是最后一行，更新尾指针
        if (当前行 == NULL) {
            文本->尾行 = 前一行;
        }
    }

    文本->已修改 = 1;
    printf("成功删除 %d 行，从第 %d 行开始\n", 实际删除行数, 起始行);
    return 1;
}

// 拷贝行函数
int 拷贝行(wen* 文本, int 源行号, int 目标行号) {
    if (源行号 < 1 || 源行号 > 文本->总行数) {
        printf("错误：源行号无效！有效范围：1-%d\n", 文本->总行数);
        return 0;
    }

    if (目标行号 < 1 || 目标行号 > 文本->总行数 + 1) {
        printf("错误：目标行号无效！有效范围：1-%d\n", 文本->总行数 + 1);
        return 0;
    }

    // 获取源行的内容
    hang* 源行 = 获取行(文本, 源行号);
    if (!源行) {
        printf("错误：找不到源行！\n");
        return 0;
    }

    // 创建新行并复制内容
    hang* 新行 = 创建新行(源行->内容);
    if (!新行) {
        printf("错误：内存不足！\n");
        return 0;
    }

    // 插入到目标位置
    if (目标行号 == 1) {
        // 插入到第一行
        新行->下一行 = 文本->首行;
        文本->首行 = 新行;
        if (文本->尾行 == NULL) {
            文本->尾行 = 新行;
        }
    }
    else if (目标行号 == 文本->总行数 + 1) {
        // 插入到最后一行
        if (文本->尾行) {
            文本->尾行->下一行 = 新行;
            文本->尾行 = 新行;
        }
        else {
            // 链表为空
            文本->首行 = 新行;
            文本->尾行 = 新行;
        }
    }
    else {
        // 插入到中间位置
        hang* 前一行 = 获取行(文本, 目标行号 - 1);
        新行->下一行 = 前一行->下一行;
        前一行->下一行 = 新行;
    }

    文本->总行数++;
    文本->已修改 = 1;
    printf("拷贝成功！将第 %d 行拷贝到第 %d 行之前\n", 源行号, 目标行号);
    return 1;
}

// 查找字符串函数
int 查找字符串函数(wen* 文本, const char* 查找字符串) {
    if (my_strlen(查找字符串) == 0) {
        printf("错误：要查找的字符串不能为空！\n");
        return 0;
    }

    int 找到次数 = 0;
    hang* 当前行 = 文本->首行;
    int 行号 = 1;

    printf("正在查找字符串 \"%s\"...\n", 查找字符串);

    while (当前行 != NULL) {
        char* 位置 = 当前行->内容;
        int 列号 = 1;

        // 在当前行中查找字符串
        while (*位置 != '\0') {
            int 匹配 = 1;
            char* 查找位置 = 位置;
            const char* 查找指针 = 查找字符串;

            // 检查是否匹配
            while (*查找指针 != '\0' && *查找位置 != '\0') {
                if (*查找位置 != *查找指针) {
                    匹配 = 0;
                    break;
                }
                查找位置++;
                查找指针++;
            }

            // 如果匹配成功且查找字符串已到末尾
            if (匹配 && *查找指针 == '\0') {
                找到次数++;
                printf("在第 %d 行第 %d 列找到 \"%s\"\n", 行号, 列号, 查找字符串);
            }

            位置++;
            列号++;
        }

        当前行 = 当前行->下一行;
        行号++;
    }

    if (找到次数 == 0) {
        printf("未找到字符串 \"%s\"\n", 查找字符串);
    }
    else {
        printf("总共找到 %d 次\n", 找到次数);
    }

    return 找到次数 > 0;
}

// 替换字符串函数
int 替换字符串函数(wen* 文本, const char* 查找字符串, const char* 替换字符串) {
    if (my_strlen(查找字符串) == 0) {
        printf("错误：要查找的字符串不能为空！\n");
        return 0;
    }

    int 替换次数 = 0;
    hang* 当前行 = 文本->首行;
    int 行号 = 1;

    printf("正在将 \"%s\" 替换为 \"%s\"...\n", 查找字符串, 替换字符串);

    while (当前行 != NULL) {
        char 新内容[MAX_行];
        新内容[0] = '\0';
        char* 位置 = 当前行->内容;

        // 遍历当前行的内容
        while (*位置 != '\0') {
            int 匹配 = 1;
            char* 查找位置 = 位置;
            const char* 查找指针 = 查找字符串;

            // 检查是否匹配
            while (*查找指针 != '\0' && *查找位置 != '\0') {
                if (*查找位置 != *查找指针) {
                    匹配 = 0;
                    break;
                }
                查找位置++;
                查找指针++;
            }

            // 如果匹配成功且查找字符串已到末尾
            if (匹配 && *查找指针 == '\0') {
                // 添加替换字符串
                my_strcat(新内容, 替换字符串);
                位置 += my_strlen(查找字符串);
                替换次数++;
            }
            else {
                // 添加当前字符
                char 临时[2] = { *位置, '\0' };
                my_strcat(新内容, 临时);
                位置++;
            }
        }

        // 如果当前行有替换发生，更新行内容
        if (my_strcmp(当前行->内容, 新内容) == 0) {
            my_strcpy(当前行->内容, 新内容);
        }

        当前行 = 当前行->下一行;
        行号++;
    }

    if (替换次数 > 0) {
        文本->已修改 = 1;
        printf("总共替换 %d 次\n", 替换次数);
    }
    else {
        printf("未找到要替换的字符串 \"%s\"\n", 查找字符串);
    }

    return 替换次数 > 0;
}